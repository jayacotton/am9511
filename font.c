/* given a character, return a pointer to
the bitmap for that character */
/* chars have a value of 0x30 to 0x3a */
/* bitmap spaceing is 12 bytes per character */

int index;
char *
getbitmap (char c)
{
  if (c <= 0xf)
    {
      index = c * 12;
      return (&bitmap[index]);
    }
  if (c == '.')
    {
	index = 16*12;
      return (&bitmap[index]);
    }
  if (c == ':')
    {
	index = 17*12;
      return (&bitmap[index]);
    }
  index = (c - 0x30) * 12;
  return (&bitmap[index]);
}
